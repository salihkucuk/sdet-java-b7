public class Swap {
	public static void main(String[] args) {
		String sky="yellow";
		String sun="blue";
		String swap;
		swap=sky;
		sky=sun;
		sun=swap;
		
		
		//TODO swap values of these two variables without direct reassignment.
		
		System.out.println("Sky is "+ sky);
		System.out.println("Sun is "+ sun);	
	}
}
