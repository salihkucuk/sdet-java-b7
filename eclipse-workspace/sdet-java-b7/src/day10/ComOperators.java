package day10;

public class ComOperators {
	public static void main(String[]args) {
		int i=12;
		int j=14;
		
		boolean isEqual=j ==i;
		System.out.println(isEqual);
		
		boolean notEqual= j != i;
		System.out.println(notEqual);
		
		
		// Greater >
		// Greater or equal >=
		System.out.println(j>i);
		
		int num =9;
		int num2=9;
		System.out.println(num>=num2);
		System.out.println(num>num2);
		
		
		
	}

}
