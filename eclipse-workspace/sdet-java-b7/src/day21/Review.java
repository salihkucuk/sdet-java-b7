package day21;

public class Review {
	public static void main(String[] args) {
		// while loop
		// do while loop
		
		// while loop vs do while loop
		// in while loop the condition goes first and then body
		// in do while loop the body goes first and then condition
		// do while loop will have at least on iteration, but while loop
		// can have 0 iterations.
		
		// TODO warm up task - write a program that prints numbers from
		// 1 - 100 in the new line by using while loop
		int count=1;
		while(count<=100) {
			System.out.println(count);
			count++;
		}
	}
}