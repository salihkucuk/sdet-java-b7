package day22;

public class WarmUp {
	public static void main(String[] args) {
		// print numbers from 1 till 50 inclusive by using for loop in one line
		// 1 2 3 4 5 6 7 8 ... 50
		for(int i=1; i<=50; i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		for(int i=50; i>=10; i--) {
			System.out.print(i+" ");
		}
		
		System.out.println();
		// print numbers from 50 till 10 inclusive by using for loop in one line
		// 50 49 48 47 ... 10
	}
}