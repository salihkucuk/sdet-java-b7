package day41;

public class Student {
	public String name;
	protected String course;
	double grade;
	private int age;
	
	// Access modifiers control the access to properties, methods, and classes
	// public    - accessible from everywhere
	// protected - accessible within the same package and subclass
	// default   - accessible within the same package only (we don't put any access modifier for default)
	// private   - accessible within the same class
}